# Number 数字 
# 整数、浮点数、复数、布尔值

# 整数
print(123)

# 浮点数
print(3.14)

# 复数
print(1 + 2j)  # 1 + 2i  

# 布尔值
print(True)
print(False)  

# 注意：整数、浮点数、复数都属于数字类型，布尔值属于逻辑类型。

# 数值运算
print(2 + 3)  # 5
print(2 - 3)  # -1
print(2 * 3)  # 6
print(2 / 3)  # 0.6666
print(2 // 3)  # 0 取整除
print(2 % 3)  # 2 取余
print(2 ** 3)  # 8 乘方

# 数值类型转换
print(int(3.14))  # 3
print(float(2))  # 2.0
print(complex(1, 2))  # (1+2j)
print(bool(1))  # True
print(bool(0))  # False 

# 数值类型比较
print(1 < 2)  # True
print(1 > 2)  # False
print(1 == 2)  # False
print(1 != 2)  # True
print(1 <= 2)  # True
print(1 >= 2)  # False

# 数值类型逻辑运算
print(True and True)  # True
print(True and False)  # False
print(False and True)  # False
print(False and False)  # False
print(True or True)  # True
print(True or False)  # True
print(False or True)  # True
print(False or False)  # False
print(not True)  # False
print(not False)  # True


